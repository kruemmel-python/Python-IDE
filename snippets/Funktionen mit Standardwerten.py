"""Modul mit einer Begrüßungsfunktion, die einen Namen und optional eine Begrüßung akzeptiert."""

def greet(name, greeting="Hallo"):
    """Begrüßt eine Person mit einem vorgegebenen oder benutzerdefinierten Gruß."""
    print(f"{greeting}, {name}!")

# Beispielaufruf der Funktion
greet("Welt")  # Ausgabe: Hallo, Welt!
